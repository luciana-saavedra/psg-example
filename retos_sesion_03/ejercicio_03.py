print("Tutorial: ¿Cómo lavar los platos correctamente?")

print("1. Elige la esponja adecuada:")
print("   - Usa una esponja suave para platos delicados (como vasos o vajilla fina).")
print("   - Para ollas y sartenes con suciedad pegada, usa una esponja con estropajo o una esponja especial para fregaderos.")
print("   - Evita usar estropajos metálicos en superficies antiadherentes para no dañarlas.")

print("2. Prepara el área de trabajo:")
print("   - Reúne todos los platos, cubiertos y utensilios sucios.")
print("   - Retira restos grandes de comida antes de lavar para evitar tapar el desagüe.")

print("3. Enjuaga cada plato con agua tibia:")
print("   - Esto ayuda a quitar residuos sueltos y facilita la limpieza.")

print("4. Llena el fregadero con agua caliente y añade detergente:")
print("   - El agua caliente ayuda a eliminar grasas.")
print("   - Usa una cantidad adecuada de detergente, no demasiada para evitar exceso de espuma.")

print("5. Orden para lavar los platos:")
print("   a) Primero los vasos y tazas, ya que son los menos sucios.")
print("   b) Luego cubiertos y utensilios.")
print("   c) Después los platos y bandejas.")
print("   d) Finalmente ollas y sartenes, que suelen tener más grasa y suciedad.")

print("6. Cómo lavar:")
print("   - Usa la esponja adecuada para cada tipo de objeto.")
print("   - Frota bien todas las superficies, bordes y esquinas.")
print("   - Para suciedad difícil, deja remojando unos minutos antes de frotar.")

print("7. Enjuaga con agua caliente:")
print("   - Asegúrate de eliminar todo el jabón y restos de suciedad.")
print("   - Usa agua corriente y revisa que no quede espuma.")

print("8. Secado:")
print("   - Coloca los platos en un escurridor para que se sequen al aire.")
print("   - O seca con un paño limpio y seco si necesitas guardarlos rápido.")
print("   - Siempre usa un paño limpio para evitar contaminación.")

print("9. Limpieza final:")
print("   - Lava y enjuaga la esponja después de usarla y déjala secar.")
print("   - Limpia el fregadero para mantener la cocina higiénica.")
print("   - Lava tus manos con jabón y sécalas bien.")

print("¡listo!, ahora sabes cómo lavar los platos de forma eficiente y ordenada")