print("6. Al colocar una palabra, se suman los puntos de cada letra más los bonos del tablero.")
print("7. Los jugadores reponen sus fichas después de cada turno.")
print("8. Gana quien obtenga más puntos cuando ya no quedan fichas para jugar.")
print("9. No se permiten palabras inventadas; se debe usar un diccionario oficial.")
print("10. Si un jugador pasa su turno 6 veces seguidas, el juego termina.")