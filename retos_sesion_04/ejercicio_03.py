print("2. Cada jugador recibe 7 fichas con letras al inicio del juego.")
print("3. El objetivo es formar palabras sobre el tablero para ganar puntos.")
print("4. Las palabras deben colocarse en horizontal o vertical, como en un crucigrama.")
